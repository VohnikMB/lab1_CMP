    for char in input_string:
        if char in banner3:
            char_ascii_art = banner3[char].split('\n')
            for i in range(7):
                output_lines[i] += char_ascii_art[i] + "  "
        else:
            for i in range(7):
                output_lines[i] += "Character not found in the 'banner3' dictionary."

    result = ""
    for line in output_lines:
        result += line + "\n"

    return result